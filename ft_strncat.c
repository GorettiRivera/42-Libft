#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		len;
	size_t	i;

	i = 0;
	len = ft_strlen(s1);
	while (s2[i] != 0 && i < n)
	{
		s1[len++] = s2[i++];
	}
	s1[len] = '\0';
	return (s1);
}
