#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, unsigned int n)
{
	if (!n)
	{
		return (0);
	}
	while ((*s1 && (*s1 == *s2)) && --n)
	{
		if (*s1 != *s2)
		{
			return (*s1 - *s2);
		}
		s1 += 1;
		s2 += 1;
	}
	return (*s1 - *s2);
}
